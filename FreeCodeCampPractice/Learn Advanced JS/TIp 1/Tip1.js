/* Tip 1: Use "console.table()" to Display Arrays and Objects in the Console */

/* console.log will display the array or object as usual,
 but console.table will tabularize the index and values for you. */


 const myArr = ['Kolade', 'Chelsea', 10, true];

console.log(myArr);  // it will display the data as usual  
console.table(myArr);  // it will display the data into index and value form.