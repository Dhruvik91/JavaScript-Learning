

function rowCheck(arr) {
    let row = new Set();
    let column = new Set();
    let rowElement;
    let columnElement;

    for (let i = 0; i < 9; i++) {  

        for (let j = 0; j < 9; j++) {

            rowElement = arr[i][j];


            if (rowElement !== ".") {

                if (row.has(rowElement)) return "Invalid";

            } row.add(rowElement);

            columnElement = arr[j][i];

            if (columnElement !== ".") {

                if (column.has(columnElement)) return "Invalid";

            } column.add(columnElement);

        } row.clear(); column.clear();
    }
}


function travsalCheck(arr) {
    let box = new Set();
    let boxElement;


    for (let i = 0; i < 9; i += 3) {

        for (let j = 0; j < 9; j += 3) {

            boxElement = arr[Math.floor(3 * (i / 3) + (j / 3))][3 * (i % 3) + (j % 3)];

            if (boxElement != ".") {

                if (box.has(boxElement)) return "Invalid";

            } box.add(boxElement);

        }
    } box.clear();
}





